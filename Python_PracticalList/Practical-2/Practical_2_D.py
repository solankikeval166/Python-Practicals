#Creating a new tuple
t1 =(5,2,4,1,3,2)
print("Tuple t1:",t1)

#sorted tuple
print("Tuple t1(Sorted):",sorted(t1))

#length() function will return the length of the tuple
print("length of t1 is:",len(t1))

#sum() function will return the sum of all elements in the tuple
print("Sum of all elements in tuple:",sum(t1))

#max() function will return the maximum in the tuple
print("Maximum element in tuple is:",max(t1))

#min() function will return the minimum in the tuple
print("Minimum element in tuple is:",min(t1))

#count() function will return the all occurrences of the given element
print("Occurrences of 2 in tuple is:",t1.count(2))

#index() function will return the index of the given element
print("Index of element 3 in tuple is:",t1.index(3))